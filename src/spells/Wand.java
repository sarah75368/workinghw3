package spells;

public class Wand {
	
	public void Expelliarmus() {
		System.out.println("You have disarmed your oppenent!");
	}
	public void Stupefy() {
		System.out.println("You have stunned your oppenent!");
	}
	public void Protego() {
		System.out.println("You protected yourself with an invisible shield!");
	}
	public void ExpectoPatronum() {
		System.out.println("You have summoned a patronus to protect you!");
	}
}